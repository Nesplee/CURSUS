#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		s1_len;
	char	*duplicate;

	if (s1 == NULL)
		return (NULL);
	s1_len = ft_strlen(s1);
	duplicate = (char *)malloc(sizeof(char) * (s1_len + 1));
	if (duplicate == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		duplicate[i] = s1[i];
		i++;
	}
	duplicate[i] = '\0';
	return (duplicate);
}
