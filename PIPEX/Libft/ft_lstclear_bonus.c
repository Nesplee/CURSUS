#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	if (lst == NULL || del == NULL)
		return ;
	while (lst[0])
	{
		tmp = lst[0]->next;
		ft_lstdelone(lst[0], del);
		lst[0] = tmp;
	}
}
